# ---------------------------------------------------------------------------
# Skip list: programs that need terminal, files, or interactive input
# ---------------------------------------------------------------------------

should_skip() {
    local key="$1"
    case "$key" in
        run_manual_screen/*) return 0 ;;
        configuration/*)     return 0 ;;
        listings/*)          return 0 ;;
        run_file/*)          return 0 ;;     # need data files
        run_accept/*)        return 0 ;;     # need stdin
        run_ml/*)            return 0 ;;     # machine-dependent
        used_binaries/*)     return 0 ;;     # utility programs
    esac
    return 1
}

has_display() {
    local cbl="$1"
    grep -qi 'DISPLAY' "$cbl" 2>/dev/null
}

is_stub() {
    local cpp="$1"
    # A stub is a file where EVERY procedure body is "TODO: LAZARUS"
    # (no real logic was generated by the transpiler)
    local total_funcs=$(grep -c "^void p_\|^void P_" "$cpp" 2>/dev/null || echo 0)
    local stub_funcs=$(grep -c "TODO: LAZARUS - Implement" "$cpp" 2>/dev/null || echo 0)
    [ "$total_funcs" -gt 0 ] && [ "$total_funcs" -eq "$stub_funcs" ]
}

