// EBCDIC collation order — specifies sort weight for each EBCDIC byte.
// Preserves IBM mainframe standard ordering: space < lower < upper < digits
inline constexpr std::array<uint16_t, 256> ebcdic_collation_weight = [] {
    std::array<uint16_t, 256> w{};
    for (int i = 0; i < 256; ++i) w[static_cast<size_t>(i)] = static_cast<uint16_t>(i);
    return w;
}();

// Convert a single EBCDIC byte to ASCII
inline constexpr uint8_t to_ascii(uint8_t eb) {
    return ebcdic_to_ascii_table[eb];
}

// Convert a single ASCII byte to EBCDIC
inline constexpr uint8_t to_ebcdic(uint8_t asc) {
    return ascii_to_ebcdic_table[asc];
}

// Convert EBCDIC string (byte buffer) to ASCII string
inline std::string ebcdic_to_ascii(const uint8_t* data, size_t len) {
    std::string result(len, '\0');
    for (size_t i = 0; i < len; ++i) {
        result[i] = static_cast<char>(ebcdic_to_ascii_table[data[i]]);
    }
    return result;
}

inline std::string ebcdic_to_ascii(std::string_view ebcdic_str) {
    return ebcdic_to_ascii(reinterpret_cast<const uint8_t*>(ebcdic_str.data()),
                           ebcdic_str.size());
}

// Convert ASCII string to EBCDIC byte buffer
inline std::string ascii_to_ebcdic(const uint8_t* data, size_t len) {
    std::string result(len, '\0');
    for (size_t i = 0; i < len; ++i) {
        result[i] = static_cast<char>(ascii_to_ebcdic_table[data[i]]);
    }
    return result;
}

inline std::string ascii_to_ebcdic(std::string_view ascii_str) {
    return ascii_to_ebcdic(reinterpret_cast<const uint8_t*>(ascii_str.data()),
                           ascii_str.size());
}

// EBCDIC collation comparison: compare two byte buffers using EBCDIC weight order
inline int ebcdic_compare(const uint8_t* a, const uint8_t* b, size_t len) {
    for (size_t i = 0; i < len; ++i) {
        auto wa = ebcdic_collation_weight[a[i]];
        auto wb = ebcdic_collation_weight[b[i]];
        if (wa < wb) return -1;
        if (wa > wb) return 1;
    }
    return 0;
}

inline int ebcdic_compare(std::string_view a, std::string_view b) {
    size_t len = std::min(a.size(), b.size());
    int c = ebcdic_compare(reinterpret_cast<const uint8_t*>(a.data()),
                           reinterpret_cast<const uint8_t*>(b.data()), len);
    if (c != 0) return c;
    if (a.size() < b.size()) return -1;
    if (a.size() > b.size()) return 1;
    return 0;
}

// Alternate collation sequence for national language support
struct AlternateCollation {
    std::array<uint16_t, 256> weights;

    AlternateCollation() : weights(ebcdic_collation_weight) {}

    // Set custom weight for a given EBCDIC code point
    void set_weight(uint8_t code_point, uint16_t weight) {
        weights[code_point] = weight;
    }

    // Compare using this alternate collation
    int compare(const uint8_t* a, const uint8_t* b, size_t len) const {
        for (size_t i = 0; i < len; ++i) {
            auto wa = weights[a[i]];
            auto wb = weights[b[i]];
            if (wa < wb) return -1;
            if (wa > wb) return 1;
        }
        return 0;
    }

    int compare(std::string_view a, std::string_view b) const {
        size_t len = std::min(a.size(), b.size());
        int c = compare(reinterpret_cast<const uint8_t*>(a.data()),
                        reinterpret_cast<const uint8_t*>(b.data()), len);
        if (c != 0) return c;
        if (a.size() < b.size()) return -1;
        if (a.size() > b.size()) return 1;
        return 0;
    }
};

// Create Danish/Norwegian alternate collation (example NLS)
inline AlternateCollation make_danish_collation() {
    AlternateCollation alt;
    // In Danish, AE ligature (0x9C in EBCDIC) sorts after Z
    alt.set_weight(0x9C, 0xF000);
    // OE ligature
    alt.set_weight(0x70, 0xF001);
    return alt;
}

// Validate EBCDIC tables are consistent inverses
inline constexpr bool validate_tables() {
    // Check printable ASCII roundtrip for key characters
    // Space
    if (ebcdic_to_ascii_table[0x40] != 0x20) return false;
    if (ascii_to_ebcdic_table[0x20] != 0x40) return false;
    // 'A'
    if (ebcdic_to_ascii_table[0xC1] != 0x41) return false;
    if (ascii_to_ebcdic_table[0x41] != 0xC1) return false;
    // '0'
    if (ebcdic_to_ascii_table[0xF0] != 0x30) return false;
    if (ascii_to_ebcdic_table[0x30] != 0xF0) return false;
    return true;
}

static_assert(validate_tables(), "EBCDIC tables must be consistent");

